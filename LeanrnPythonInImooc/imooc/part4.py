# List和Tuple类型

# 用[] 包起来的就是 list集合
list =  ['Michael','Bob','Tarcy']
print(list)

# 集合里面的类型可以不一致
list = [True, 1024, 'Part4']
print(list)

list = ['Adam', 95.5, 'Lisa', 85, 'Bart', 59]
print(list)

# 按索引访问list
print(list[0])
print(list[1])
print(list[2])

#- 1索引 表示 倒数第一个
print(list[-1])
print(list[-2])
print(list[-3])

